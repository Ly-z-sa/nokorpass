export function getTopRatedMovies(count = 3) {
  return [...moviesData]
    .filter((m) => typeof m.rating === "number")
    .sort((a, b) => b.rating - a.rating)
    .slice(0, count);
}

export function getMovieById(id) {
  return moviesData.find((m) => m.id === Number(id));
}
