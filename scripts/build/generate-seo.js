const fs = require('fs');
const path = require('path');
const { SITE_URL } = require('./site-config');

const root = path.resolve(__dirname, '../..');
const moviesDataPath = path.join(root, 'js/movies-data.js');
const moviesContent = fs.readFileSync(moviesDataPath, 'utf8');
const today = new Date().toISOString().slice(0, 10);

function parseMovies(content) {
  const movies = [];
  const re = /id:\s*(\d+),[\s\S]*?title:\s*"([^"]+)"[\s\S]*?genre:\s*"([^"]+)"[\s\S]*?image:\s*"([^"]+)"[\s\S]*?trailer:\s*"([^"]+)"[\s\S]*?synopsis:\s*"((?:[^"\\]|\\.)*)"[\s\S]*?releaseDate:\s*"([^"]+)"/g;
  let match;
  while ((match = re.exec(content)) !== null) {
    movies.push({
      id: Number(match[1]),
      title: match[2],
      genre: match[3],
      image: match[4],
      trailer: match[5],
      synopsis: match[6],
      releaseDate: match[7],
    });
  }
  return movies;
}

function escapeJsString(value) {
  return value.replace(/\\/g, '\\\\').replace(/"/g, '\\"').replace(/\n/g, ' ');
}

const movies = parseMovies(moviesContent);

const staticPages = [
  { loc: '/', changefreq: 'daily', priority: '1.0' },
  { loc: '/movies.html', changefreq: 'daily', priority: '0.9' },
  { loc: '/events.html', changefreq: 'daily', priority: '0.9' },
  { loc: '/help.html', changefreq: 'weekly', priority: '0.5' },
  { loc: '/privacy.html', changefreq: 'monthly', priority: '0.3' },
  { loc: '/terms.html', changefreq: 'monthly', priority: '0.3' },
];

function urlEntry({ loc, changefreq, priority }) {
  return `  <url>
    <loc>${SITE_URL}${loc}</loc>
    <lastmod>${today}</lastmod>
    <changefreq>${changefreq}</changefreq>
    <priority>${priority}</priority>
  </url>`;
}

const moviePages = movies.map((movie) => ({
  loc: `/movie-details.html?id=${movie.id}`,
  changefreq: 'weekly',
  priority: '0.8',
}));

const sitemap = `<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">
${[...staticPages, ...moviePages].map(urlEntry).join('\n\n')}
</urlset>
`;

fs.writeFileSync(path.join(root, 'sitemap.xml'), sitemap, 'utf8');

const movieSeoEntries = movies
  .map((movie) => {
    const shortSynopsis = movie.synopsis.slice(0, 150);
    return `  ${movie.id}: { t: "${escapeJsString(movie.title)}", d: "${escapeJsString(shortSynopsis)}", i: "${escapeJsString(movie.image)}", g: "${escapeJsString(movie.genre)}", r: "${movie.releaseDate}", tr: "${escapeJsString(movie.trailer)}" }`;
  })
  .join(',\n');

const movieSeoHead = `/* Auto-generated by scripts/build/generate-seo.js — do not edit manually */
(function () {
  var SITE = "${SITE_URL}";
  var movies = {
${movieSeoEntries}
  };

  var id = parseInt(new URLSearchParams(location.search).get("id"), 10) || 1;
  var movie = movies[id];
  if (!movie) return;

  var pageUrl = SITE + "/movie-details.html?id=" + id;
  var imageUrl = SITE + "/assets/" + movie.i;
  var description = "Book tickets for " + movie.t + " on NokorPass. Genre: " + movie.g + ". " + movie.d + "...";

  document.title = movie.t + " \\u2014 NokorPass";

  function setMeta(selector, attr, value) {
    var el = document.querySelector(selector);
    if (el) el.setAttribute(attr, value);
  }

  setMeta("#seoDescription", "content", description);
  setMeta("#seoCanonical", "href", pageUrl);
  setMeta('meta[property="og:title"]', "content", movie.t + " \\u2014 NokorPass");
  setMeta('meta[property="og:description"]', "content", movie.d);
  setMeta('meta[property="og:image"]', "content", imageUrl);
  setMeta('meta[property="og:url"]', "content", pageUrl);
  setMeta('meta[name="twitter:title"]', "content", movie.t + " \\u2014 NokorPass");
  setMeta('meta[name="twitter:description"]', "content", movie.d);
  setMeta('meta[name="twitter:image"]', "content", imageUrl);

  var jsonLd = document.getElementById("movieJsonLd");
  if (jsonLd) {
    jsonLd.textContent = JSON.stringify({
      "@context": "https://schema.org",
      "@type": "Movie",
      name: movie.t,
      description: movie.d,
      image: imageUrl,
      genre: movie.g,
      datePublished: movie.r,
      url: pageUrl,
      trailer: {
        "@type": "VideoObject",
        name: movie.t + " Trailer",
        embedUrl: movie.tr
      }
    });
  }
})();
`;

fs.writeFileSync(path.join(root, 'js/movie-seo-head.js'), movieSeoHead, 'utf8');

const itemListSchema = {
  '@context': 'https://schema.org',
  '@type': 'ItemList',
  name: 'Movies on NokorPass',
  url: `${SITE_URL}/movies.html`,
  numberOfItems: movies.length,
  itemListElement: movies.map((movie, index) => ({
    '@type': 'ListItem',
    position: index + 1,
    url: `${SITE_URL}/movie-details.html?id=${movie.id}`,
    name: movie.title,
  })),
};

const itemListBlock = `  <script type="application/ld+json">
${JSON.stringify(itemListSchema, null, 2).split('\n').map((line) => '  ' + line).join('\n').trim()}
  </script>`;

const moviesHtmlPath = path.join(root, 'movies.html');
let moviesHtml = fs.readFileSync(moviesHtmlPath, 'utf8');
const itemListStart = '<!-- seo:movies-itemlist:start -->';
const itemListEnd = '<!-- seo:movies-itemlist:end -->';

if (moviesHtml.includes(itemListStart) && moviesHtml.includes(itemListEnd)) {
  moviesHtml = moviesHtml.replace(
    new RegExp(`${itemListStart}[\\s\\S]*?${itemListEnd}`),
    `${itemListStart}\n${itemListBlock}\n  ${itemListEnd}`
  );
  fs.writeFileSync(moviesHtmlPath, moviesHtml, 'utf8');
}

fs.writeFileSync(
  path.join(root, 'js/seo-config.js'),
  `/** Shared SEO constants — auto-generated from scripts/build/site-config.js */\nexport const SITE_URL = '${SITE_URL}';\nexport const DEFAULT_OG_IMAGE = '${SITE_URL}/assets/spiderman-brandnewday.jpg';\n`,
  'utf8'
);

console.log(`SEO assets generated: sitemap (${staticPages.length + moviePages.length} URLs), movie-seo-head.js (${movies.length} movies), movies ItemList.`);
